import turtle 
def drawS(theTurtle):
    theTurtle.forward(100)
    theTurtle.left(90)
    theTurtle.forward(100)
    theTurtle.left(90)
    theTurtle.forward(100)
    theTurtle.right(90)
    theTurtle.forward(100)
    theTurtle.right(90)
    theTurtle.forward(100)
myTurtle = turtle.Turtle()  # Create a new Turtle object
drawS(myTurtle)   # make the new Turtle draw the shape




